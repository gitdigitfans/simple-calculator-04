export type TranslationKey = keyof typeof LANG['ar'];

let _currentLang: Lang = 'ar';

export function getCurrentLang(): Lang {
  return _currentLang;
}

export function t(key: TranslationKey, lang?: Lang): string {
  return LANG[lang || _currentLang][key] || key;
}

export function setLang(lang: Lang): void {
  if (LANG[lang]) {
    _currentLang = lang;
  }
}

export function initLang(): Lang {
  if (typeof window !== 'undefined' && typeof localStorage !== 'undefined') {
    const saved = localStorage.getItem('cuteKidsLang') as Lang | null;
    if (saved && LANG[saved]) {
      _currentLang = saved;
    }
  }
  return _currentLang;
}
